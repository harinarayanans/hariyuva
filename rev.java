import java.util.*;
import java.lang.*;
import java.io.*;
class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner s=new Scanner(System.in);
		int n=s.nextInt();
		while(n!=0)
		{
		    int temp=n%10;
		    System.out.print(temp);
		    n=n/10;
		    
		}
	}
}
